#include "I2C_Driver.h"

/*********************************************************************
 *                  Constructor
 *********************************************************************/

I2C::I2C(I2C_Instance instance)
{
    switch (instance)
    {
        case I2C_Instance::I2C1:
            mI2C = I2C1_REG;
            break;

        case I2C_Instance::I2C2:
            mI2C = I2C2_REG;
            break;

        case I2C_Instance::I2C3:
            mI2C = I2C3_REG;
            break;
    }
}

/*********************************************************************
 *                  Enable I2C Peripheral Clock
 *********************************************************************/

void I2C::enableClock()
{
    RCC rcc;

    if (mI2C == I2C1_REG)
    {
        rcc.enableI2CClock(RCC_I2C::I2C1);
    }
    else if (mI2C == I2C2_REG)
    {
        rcc.enableI2CClock(RCC_I2C::I2C2);
    }
    else if (mI2C == I2C3_REG)
    {
        rcc.enableI2CClock(RCC_I2C::I2C3);
    }
}

/*********************************************************************
 *                  Enable I2C Peripheral
 *********************************************************************/

void I2C::enable()
{
    /*
     * CR1
     * Bit 0 : PE (Peripheral Enable)
     */

    mI2C->CR1 |= (1U << 0);
}

/*********************************************************************
 *                  Disable I2C Peripheral
 *********************************************************************/

void I2C::disable()
{
    /*
     * CR1
     * Bit 0 : PE (Peripheral Enable)
     */

    mI2C->CR1 &= ~(1U << 0);
}

/*********************************************************************
 *                  Initialize I2C Peripheral
 *********************************************************************/

void I2C::init(uint32_t clockSpeed,
               I2C_ACK ack)
{
    /**************************************************************
     * Enable Peripheral Clock
     **************************************************************/

    enableClock();

    /**************************************************************
     * Disable I2C before configuration
     **************************************************************/

    disable();

    /**************************************************************
     * Configure APB1 Frequency
     *
     * CR2[5:0]
     *
     * APB1 Clock = 16 MHz
     **************************************************************/

    mI2C->CR2 &= ~(0x3FU);
    mI2C->CR2 |= 16U;

    /**************************************************************
     * Configure Clock Control Register
     *
     * Standard Mode (<=100kHz)
     *
     * CCR = Fpclk1 / (2 * I2C Speed)
     *
     * Example:
     *
     * Fpclk1 = 16 MHz
     * Speed  = 100 kHz
     *
     * CCR = 16000000 / (2 * 100000)
     *     = 80
     **************************************************************/

    uint16_t ccr = static_cast<uint16_t>(
            16000000U / (2U * clockSpeed));

    mI2C->CCR = ccr;

    /**************************************************************
     * Configure Maximum Rise Time
     *
     * Standard Mode
     *
     * TRISE = Fpclk(MHz) + 1
     *
     * = 16 + 1
     * = 17
     **************************************************************/

    mI2C->TRISE = 17U;

    /**************************************************************
     * Configure ACK
     *
     * CR1 Bit10
     **************************************************************/

    if (ack == I2C_ACK::ENABLE)
    {
        mI2C->CR1 |= (1U << 10);
    }
    else
    {
        mI2C->CR1 &= ~(1U << 10);
    }

    /**************************************************************
     * Enable I2C Peripheral
     **************************************************************/

    enable();
}

/*********************************************************************
 *                  Generate START Condition
 *********************************************************************/

void I2C::generateStart()
{
    /*
     * CR1 Bit8 : START
     */

    mI2C->CR1 |= (1U << 8);

    /*
     * Wait until START condition generated
     *
     * SR1 Bit0 : SB
     */

    while (!(mI2C->SR1 & (1U << 0)));
}


/*********************************************************************
 *                  Generate STOP Condition
 *********************************************************************/

void I2C::generateStop()
{
    /*
     * CR1 Bit9 : STOP
     */

    mI2C->CR1 |= (1U << 9);
}


/*********************************************************************
 *                  Send Slave Address
 *********************************************************************/

void I2C::sendAddress(uint8_t address,
                      bool read)
{
    /*
     * 7-bit Address
     *
     * Bit0 = R/W
     */

    uint8_t addr = (address << 1);

    if (read)
    {
        addr |= 0x01;
    }

    /*
     * Write Address
     */

    mI2C->DR = addr;

    /*
     * Wait until Address Sent
     *
     * SR1 Bit1 : ADDR
     */

    while (!(mI2C->SR1 & (1U << 1)));

    /*
     * Clear ADDR flag
     *
     * Read SR1 followed by SR2
     */

    volatile uint32_t temp;

    temp = mI2C->SR1;
    temp = mI2C->SR2;

    (void)temp;
}


/*********************************************************************
 *                  Send One Byte
 *********************************************************************/

void I2C::sendByte(uint8_t data)
{
    /*
     * Wait until TXE = 1
     */

    while (!(mI2C->SR1 & (1U << 7)));

    mI2C->DR = data;

    /*
     * Wait until Byte Transfer Finished
     *
     * SR1 Bit2 : BTF
     */

    while (!(mI2C->SR1 & (1U << 2)));
}


/*********************************************************************
 *                  Receive One Byte
 *********************************************************************/

uint8_t I2C::receiveByte()
{
    /*
     * Wait until RXNE = 1
     */

    while (!(mI2C->SR1 & (1U << 6)));

    return static_cast<uint8_t>(mI2C->DR);
}

/*********************************************************************
 *                  Master Transmit
 *********************************************************************/

void I2C::masterTransmit(uint8_t slaveAddress,
                         const uint8_t* buffer,
                         uint32_t length)
{
    /**************************************************************
     * Generate START
     **************************************************************/
    generateStart();

    /**************************************************************
     * Send Slave Address (Write)
     **************************************************************/
    sendAddress(slaveAddress, false);

    /**************************************************************
     * Send Data Bytes
     **************************************************************/
    while (length)
    {
        sendByte(*buffer);

        buffer++;

        length--;
    }

    /**************************************************************
     * Generate STOP
     **************************************************************/
    generateStop();
}


/*********************************************************************
 *                  Master Receive
 *********************************************************************/

void I2C::masterReceive(uint8_t slaveAddress,
                        uint8_t* buffer,
                        uint32_t length)
{
    /**************************************************************
     * Generate START
     **************************************************************/
    generateStart();

    /**************************************************************
     * Send Slave Address (Read)
     **************************************************************/
    sendAddress(slaveAddress, true);

    /**************************************************************
     * Receive Data Bytes
     **************************************************************/
    while (length)
    {
        *buffer = receiveByte();

        buffer++;

        length--;
    }

    /**************************************************************
     * Generate STOP
     **************************************************************/
    generateStop();
}

/*********************************************************************
 *                  START Generated
 *********************************************************************/

bool I2C::isStartGenerated()
{
    return (mI2C->SR1 & (1U << 0));
}

/*********************************************************************
 *                  Address Sent
 *********************************************************************/

bool I2C::isAddressSent()
{
    return (mI2C->SR1 & (1U << 1));
}

/*********************************************************************
 *                  TXE Flag
 *********************************************************************/

bool I2C::isTXE()
{
    return (mI2C->SR1 & (1U << 7));
}

/*********************************************************************
 *                  RXNE Flag
 *********************************************************************/

bool I2C::isRXNE()
{
    return (mI2C->SR1 & (1U << 6));
}

/*********************************************************************
 *                  Byte Transfer Finished
 *********************************************************************/

bool I2C::isTransferFinished()
{
    return (mI2C->SR1 & (1U << 2));
}

/*********************************************************************
 *                  Bus Busy
 *********************************************************************/

bool I2C::isBusy()
{
    return (mI2C->SR2 & (1U << 1));
}